<section class="card" style="margin-top: 16px;">
  <h2>1. Какие данные мы обрабатываем</h2>
  <ul>
    <li>Регистрационные данные: имя, email, пароль (в хэшированном виде).</li>
    <li>Данные аккаунта и профиля пользователя.</li>
    <li>Данные устройств, датчиков и действий в системе (например, журнал ухода за растениями).</li>
    <li>Технические данные, необходимые для работы сервиса (сессии, служебные логи ошибок).</li>
  </ul>

  <h2>2. Цели обработки данных</h2>
  <ul>
    <li>Предоставление доступа к функциям платформы.</li>
    <li>Отображение показаний датчиков и управление устройствами.</li>
    <li>Повышение надежности и безопасности сервиса.</li>
    <li>Связь с пользователем по вопросам работы аккаунта.</li>
  </ul>

  <h2>3. Передача данных третьим лицам</h2>
  <p>
    Мы не продаем персональные данные. Передача возможна только в рамках технической необходимости
    (например, почтовый провайдер для отправки сервисных писем) или когда этого требует законодательство.
  </p>

  <h2>4. Хранение и защита данных</h2>
  <ul>
    <li>Пароли хранятся в виде криптографических хэшей.</li>
    <li>Используются базовые меры защиты сессий и проверка CSRF-токенов в формах.</li>
    <li>Доступ к данным ограничен правами пользователя в рамках приложения.</li>
  </ul>

  <h2>5. Права пользователя</h2>
  <ul>
    <li>Запросить информацию о хранимых данных.</li>
    <li>Обновить или исправить данные профиля.</li>
    <li>Запросить удаление аккаунта и связанных данных.</li>
  </ul>

